const DICTS: Record<LangCode, Dict> = { id, en, zh, ja, ko, de, es, fr, pt };

const STORAGE_KEY = "morrighans:lang";

type I18nState = {
  lang: LangCode;
  setLang: (l: LangCode) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nState | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>("id");

  useEffect(() => {
    if (typeof window === "undefined") return;
    const saved = window.localStorage.getItem(STORAGE_KEY) as LangCode | null;
    if (saved && DICTS[saved]) {
      setLangState(saved);
    } else {
      const browser = (typeof navigator !== "undefined" ? navigator.language : "id").slice(0, 2).toLowerCase();
      if ((DICTS as Record<string, Dict>)[browser]) setLangState(browser as LangCode);
    }
  }, []);

  const setLang = useCallback((l: LangCode) => {
    setLangState(l);
    if (typeof window !== "undefined") window.localStorage.setItem(STORAGE_KEY, l);
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = DICTS[lang] ?? DICTS.en;
      let s = dict[key] ?? DICTS.en[key] ?? key;
      if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, String(v));
      return s;
    },
    [lang],
  );

  const value = useMemo<I18nState>(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (ctx) return ctx;
  // Fallback when used outside provider (e.g. error/notfound boundaries)
  const t = (key: string, vars?: Record<string, string | number>) => {
    let s = DICTS.en[key] ?? key;
    if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, String(v));
    return s;
  };
  return { lang: "en" as LangCode, setLang: () => {}, t };
}
